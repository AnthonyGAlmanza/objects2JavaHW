package objects2JavaHW;

public class Calculator {
	
	public static int toAdd(int numOne, int numTwo) {
		int sum = numOne + numTwo;
		return sum;
	}
	
	public static int toSubtract(int numOne, int numTwo) {
		int sum = numOne - numTwo;
		return sum;
	}
	
	public static int toMultiply(int numOne, int numTwo) {
		int sum = numOne * numTwo;
		return sum;
	}
	
	public static int toDivide(int numOne, int numTwo) {
		int sum = numOne / numTwo;
		return sum;
	}

	public static int toSquare(int numOne) {
		int square = numOne * numOne;
		return square;
	}
	
	
}
